# Basic if statement
x = 10
if x > 5:
    print("x is greater than 5")

# if-else statement
x = 3
if x > 5:
    print("x is greater than 5")
else:
    print("x is 5 or less")

# if-elif-else statement
x = 10
if x > 10:
    print("x is greater than 10")
elif x == 10:
    print("x is exactly 10")
else:
    print("x is less than 10")
