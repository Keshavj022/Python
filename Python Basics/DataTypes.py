# Subscripting in Strings
print("Hello"[0])  # Outputs: H
print("Hello"[-1]) # Outputs: o
print("Hello"[4])  # Outputs: o
print("1234" + "5678")  # Concatenating strings

# Integers
print(1233445)

# Large Integers (underscore for readability)
print(123_456_789)

# Float
print(123.89)

# Boolean
print(True)
print(False)